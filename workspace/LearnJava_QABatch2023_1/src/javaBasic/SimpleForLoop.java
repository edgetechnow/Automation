package javaBasic;

public class SimpleForLoop {
	public static void main(String[] args) {
		
		
		
		//for(initialization; expression/condition; increment  ){ body }
		
		// This for loop counts from 1 to 10
		//increment happens by increasing 1
		
	
		
		for (int count = 1; count <=5; count++) {//iteration start here
			
			System.out.println("my value: " +count );
			
			System.out.println("Hasan");
			
			
			
			//loop body
			
			
			
		}//iteration ends here
		
		
	}
}
